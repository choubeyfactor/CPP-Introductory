#include <iostream>;

using namespace std;
int main(){
    int a=4 , b=7;
    cout << "Operators in C++:";
    cout << "The sum of a and b is: " << b+a << endl;
    cout << "The substraction of band a is: " << b-a << endl;
    cout << "The multiplication of a and b is: " << b*a << endl;
    cout << "The division of band a is: " << b/a << endl;
    cout << "The module of a and b is: "<< b%a << endl ;
    cout << "The value of a++ is: " << a++ << endl;
    cout << "The value of a-- is: " << a-- << endl;
    cout << "The value of ++a is: " << ++a << endl;
    cout << "The value of a-- is: " << --a << endl;
    return 0;}
